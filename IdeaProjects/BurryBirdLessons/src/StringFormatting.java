public class StringFormatting {
    public static void main(String[] args) {
        System.out.printf("This is a string, %s", "Nice");
        System.out.println();
        System.out.printf("This is a string, %d", 10);
        System.out.println("");
        System.out.println("");
        System.out.println("");
        System.out.println("");
    }
}
